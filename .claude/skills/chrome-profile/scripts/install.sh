#!/bin/bash
# install.sh — install the chrome-profile CLI shim onto PATH.
#
# Creates a small shim at $PREFIX/bin/chrome-profile that invokes the Python CLI
# bundled in this skill. Idempotent. Default prefix is $HOME/.local.
#
# Usage:
#   ./install.sh                    # install to $HOME/.local/bin
#   PREFIX=/usr/local ./install.sh  # custom prefix
set -euo pipefail

SCRIPT_DIR="$( cd "$( dirname "${BASH_SOURCE[0]}" )" && pwd )"
CLI="$SCRIPT_DIR/chrome_profile_cli.py"
PREFIX="${PREFIX:-$HOME/.local}"
BIN_DIR="$PREFIX/bin"
SHIM="$BIN_DIR/chrome-profile"

if [[ ! -f "$CLI" ]]; then
  echo "install.sh: cannot find $CLI" >&2
  exit 1
fi

if ! command -v python3 >/dev/null 2>&1; then
  echo "install.sh: python3 not on PATH" >&2
  exit 1
fi

mkdir -p "$BIN_DIR"

cat > "$SHIM" <<EOF
#!/bin/bash
# auto-generated by chrome-profile skill install.sh
exec python3 "$CLI" "\$@"
EOF
chmod +x "$SHIM"

echo "[+] installed: $SHIM -> $CLI"
case ":$PATH:" in
  *":$BIN_DIR:"*) ;;
  *) echo "[!] $BIN_DIR is not on PATH — add it to your shell rc:"; echo "    export PATH=\"$BIN_DIR:\$PATH\"" ;;
esac
echo "[*] next: chrome-profile setup"
